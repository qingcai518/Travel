import Foundation
import UIKit

extension UIFont {
    static func Helvetica12() -> UIFont {
        return UIFont(name: "Helvetica", size: 12)!
    }
    
    static func Helvetica14() -> UIFont {
        return UIFont(name: "Helvetica", size: 14)!
    }
    
    static func Helvetica16() -> UIFont {
        return UIFont(name: "Helvetica", size: 16)!
    }
    
    static func Helvetica18() -> UIFont {
        return UIFont(name: "Helvetica", size: 18)!
    }
    
    static func Helvetica12Bold() -> UIFont {
        return UIFont(name: "Helvetica-Bold", size: 12)!
    }
    
    static func Helvetica14Bold() -> UIFont {
        return UIFont(name: "Helvetica-Bold", size: 14)!
    }
    
    static func Helvetica16Bold() -> UIFont {
        return UIFont(name: "Helvetica-Bold", size: 16)!
    }
    
    static func Helvetica18Bold() -> UIFont {
        return UIFont(name: "Helvetica-Bold", size: 18)!
    }
}
